// 隐私兜底：即使上游意外回显了简历里的手机号/邮箱/证件号，渲染前也再遮一层。
export function maskPII(s: string): string {
  return s
    .replace(/[\w.+-]+@[\w-]+\.[\w.-]+/g, "[邮箱已隐藏]")
    .replace(/(?<!\d)\d{17}[\dXx](?!\d)/g, "[证件号已隐藏]") // 身份证 18 位
    .replace(/(?<!\d)(?:\+?86[-\s]?)?1[3-9]\d{9}(?!\d)/g, "[手机号已隐藏]") // 国内手机
    .replace(/\+\d{1,3}[-\s]?\d{6,14}(?!\d)/g, "[电话已隐藏]") // 带国家码的国际电话
    .replace(/(?<![A-Za-z0-9])[A-Za-z]{1,2}\d{7,8}(?![A-Za-z0-9])/g, "[证件号已隐藏]"); // 护照等 字母+数字
}
